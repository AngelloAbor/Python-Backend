### type hints 

my_string = "Hello, World!"

print(my_string)
print(type(my_string))

my_string = 5

print(my_string)
print(type(my_string))

my_typed_variable: str = "my type string variable"
print(my_typed_variable)
print(type(my_typed_variable))

my_typed_variable: int = "my type string variable"
print(my_typed_variable)
print(type(my_typed_variable))